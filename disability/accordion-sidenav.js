$("ul.vertical-nav > li:nth-of-type(4) > a").click(function () {
  var current = 'collapse8502';
  var acc = document.querySelectorAll(`[data-target='#${current}']`);
  var post = document.querySelectorAll(`#${current}`);
  $(post).addClass('in');
  $(acc).removeClass("collapsed");
  $("html, body").animate({
    scrollTop: $(acc).offset().top - 175
  }, 1500);
});

